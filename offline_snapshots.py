#!/usr/bin/env python3
"""
Rebuild order-book snapshots from Bybit OB delta streams.

This script reads OUT_ROOT/meta.json generated by offline_ingest and rebuilds
snapshots for weeks_in_order.

Time-grid contract is centralized in CMSSL17.py.

Outputs canonical snapshots.npz files compatible with RL_exec.load_raw_snapshots.
The snapshots payload is always a 4-column top-of-book matrix:
  (best_bid, best_ask, best_bid_size, best_ask_size).
RL execution expects this exact canonical schema.

Env defaults:
  BYBIT_OB_DIR=/home/gabrool/Documents/OB
  BYBIT_OUT_ROOT=/media/gabrool/Expansion/Gabriel/bybit_offline_dt
  BYBIT_WEEKS=""  # optional comma-separated week keys
"""

from __future__ import annotations

import argparse
import importlib.util
import json
import os
import re
from dataclasses import dataclass
from datetime import date, datetime, timedelta
from pathlib import Path
from typing import Iterable, List, Optional, Tuple

import numpy as np

from CMSSL17 import (
    FeatureEngine,
    TIME_GRID_GUARD_MS,
    TIME_GRID_STEP_MS,
    _open_text,
    quantize_ts_ms,
)


# fast json if available
_orjson_spec = importlib.util.find_spec("orjson")
if _orjson_spec is not None:
    import orjson as _fastjson

    def fast_json_loads(s: str):
        return _fastjson.loads(s)
else:
    def fast_json_loads(s: str):
        return json.loads(s)


OB_DIR = os.environ.get("BYBIT_OB_DIR", "/home/gabrool/Documents/OB")
OUT_ROOT = os.environ.get("BYBIT_OUT_ROOT", "/media/gabrool/Expansion/Gabriel/bybit_offline_dt")
RAW_BYBIT_WEEKS = os.environ.get("BYBIT_WEEKS", "")
OB_DAILY_RE = re.compile(r"^(?P<d>\d{4}-\d{2}-\d{2})_BTCUSDT_.*ob.*\.zip$", re.IGNORECASE)


def _parse_requested_weeks(raw: str) -> List[str]:
    items = [wk.strip() for wk in re.split(r"[\s,]+", raw) if wk.strip()]
    return items


def _parse_ymd_date(s: str) -> date:
    return datetime.strptime(s, "%Y-%m-%d").date()


def _daily_ob_day(path_or_name: str) -> date:
    name = Path(path_or_name).name
    m = OB_DAILY_RE.match(name)
    if not m:
        raise ValueError(f"Not a daily OB filename: {name}")
    return _parse_ymd_date(m.group("d"))


def extract_week_key_from_name(name: str) -> str:
    m = re.search(r"\d{2}-\d{2}-\d{4}-to-\d{2}-\d{2}-\d{4}", name)
    if m:
        return m.group(0)
    m = re.search(r"\d{4}-\d{2}-\d{2}-to-\d{4}-\d{2}-\d{2}", name)
    if m:
        return m.group(0)
    raise ValueError(f"Could not extract week key from file name: {name}")


def _parse_week_key_any(base: str) -> Tuple[datetime, datetime, str]:
    wk = re.sub(r"^(BTCUSDT_(?:OB|TH)_)", "", base)
    wk = re.sub(r"\.(?:zip|gz|jsonl|csv)$", "", wk)
    m = re.match(r"(\d{2}-\d{2}-\d{4})-to-(\d{2}-\d{2}-\d{4})", wk)
    if m:
        s = datetime.strptime(m.group(1), "%d-%m-%Y")
        e = datetime.strptime(m.group(2), "%d-%m-%Y")
        return s, e, wk
    m = re.match(r"(\d{4}-\d{2}-\d{2})-to-(\d{4}-\d{2}-\d{2})", wk)
    if m:
        s = datetime.strptime(m.group(1), "%Y-%m-%d")
        e = datetime.strptime(m.group(2), "%Y-%m-%d")
        return s, e, wk
    raise ValueError(f"Unrecognized week key: {base}")


def _build_ob_daily_map(ob_dir: str) -> dict[date, str]:
    by_day: dict[date, str] = {}
    for path in sorted(Path(ob_dir).iterdir()):
        if not path.is_file():
            continue
        m = OB_DAILY_RE.match(path.name)
        if not m:
            continue
        day = _parse_ymd_date(m.group("d"))
        current = by_day.get(day)
        candidate = str(path)
        if current is None:
            by_day[day] = candidate
            continue
        winner = min(Path(current).name, path.name)
        if winner != Path(current).name:
            by_day[day] = candidate
        print(
            f"[warn] duplicate daily OB for {day.isoformat()}: "
            f"{Path(current).name} vs {path.name}; using {winner}"
        )
    return by_day


def iter_ob_events(ob_path: str):
    with _open_text(ob_path) as f:
        for line in f:
            if not line:
                continue
            obj = fast_json_loads(line)
            yield obj


def iter_ob_events_many(ob_paths: List[str]):
    for p in ob_paths:
        yield from iter_ob_events(p)


@dataclass
class SnapshotSeries:
    ts: List[int]
    best_bid: List[float]
    best_ask: List[float]
    best_bid_size: List[float]
    best_ask_size: List[float]
    time_since_last_ob_update_ms: List[float]

    def append(
        self,
        ts_ms: int,
        bid: float,
        ask: float,
        bid_size: float,
        ask_size: float,
        stale_ms: float,
    ) -> None:
        # Invariant: every per-row series must be appended in lockstep.
        self.ts.append(int(ts_ms))
        self.best_bid.append(float(bid))
        self.best_ask.append(float(ask))
        self.best_bid_size.append(float(bid_size))
        self.best_ask_size.append(float(ask_size))
        self.time_since_last_ob_update_ms.append(max(float(stale_ms), 0.0))

    def to_npz(self, path: Path) -> None:
        """Save canonical snapshot arrays.

        Schema:
          - ts: int64 ms timestamps
          - snapshots: float32 [N,4] = (best_bid, best_ask, best_bid_size, best_ask_size)

        Example row:
          snapshots[i] = [63125.5, 63126.0, 4.2, 3.8]

        RL execution requires this canonical 4-field format.
        """
        # Invariant: all per-row arrays must remain equal length.
        n_rows = len(self.ts)
        if not (
            n_rows
            == len(self.best_bid)
            == len(self.best_ask)
            == len(self.best_bid_size)
            == len(self.best_ask_size)
            == len(self.time_since_last_ob_update_ms)
        ):
            raise ValueError("SnapshotSeries arrays have mismatched lengths")
        snapshots = np.column_stack(
            [self.best_bid, self.best_ask, self.best_bid_size, self.best_ask_size]
        ).astype(np.float32)
        np.savez_compressed(
            path,
            ts=np.asarray(self.ts, dtype=np.int64),
            snapshots=snapshots,
            time_since_last_ob_update_ms=np.asarray(self.time_since_last_ob_update_ms, dtype=np.float32),
        )


def build_snapshots_from_ob_files(ob_paths: List[str]) -> SnapshotSeries:
    fe = FeatureEngine()
    series = SnapshotSeries(
        ts=[],
        best_bid=[],
        best_ask=[],
        best_bid_size=[],
        best_ask_size=[],
        time_since_last_ob_update_ms=[],
    )

    next_sample_ts: Optional[int] = None
    last_bid: Optional[float] = None
    last_ask: Optional[float] = None
    last_bsz: Optional[float] = None
    last_asz: Optional[float] = None
    last_ob_update_ts: Optional[int] = None

    for raw in iter_ob_events_many(ob_paths):
        etype, ts_ms_raw, payload = fe._parse_event(raw)
        ts_ms = quantize_ts_ms(ts_ms_raw, TIME_GRID_STEP_MS, TIME_GRID_GUARD_MS)
        if etype != "ob":
            continue

        if (
            last_bid is not None
            and last_ask is not None
            and last_bsz is not None
            and last_asz is not None
            and next_sample_ts is not None
            and last_ob_update_ts is not None
        ):
            while next_sample_ts < ts_ms:
                stale_ms = max(next_sample_ts - last_ob_update_ts, 0)
                series.append(next_sample_ts, last_bid, last_ask, last_bsz, last_asz, stale_ms)
                next_sample_ts += TIME_GRID_STEP_MS

        fe._update_book_from_ob(payload)
        bid, ask, bsz, asz = fe._book_best()
        # Startup policy: do not sample until all four top-of-book values are known.
        if bid <= 0.0 or ask <= 0.0 or bsz is None or asz is None:
            continue
        bsz = max(float(bsz), 0.0)
        asz = max(float(asz), 0.0)

        if series.ts and series.ts[-1] == ts_ms:
            series.best_bid[-1] = float(bid)
            series.best_ask[-1] = float(ask)
            series.best_bid_size[-1] = float(bsz)
            series.best_ask_size[-1] = float(asz)
            series.time_since_last_ob_update_ms[-1] = 0.0
            last_bid = bid
            last_ask = ask
            last_bsz = bsz
            last_asz = asz
            last_ob_update_ts = int(ts_ms)
            continue

        if next_sample_ts is None:
            next_sample_ts = int(ts_ms)

        while next_sample_ts <= ts_ms:
            stale_ms = max(next_sample_ts - ts_ms, 0)
            series.append(next_sample_ts, bid, ask, bsz, asz, stale_ms)
            next_sample_ts += TIME_GRID_STEP_MS

        last_bid = bid
        last_ask = ask
        last_bsz = bsz
        last_asz = asz
        last_ob_update_ts = int(ts_ms)

    return series


def build_snapshots_from_ob(ob_path: str) -> SnapshotSeries:
    return build_snapshots_from_ob_files([ob_path])


def load_weeks_in_order(out_root: str) -> List[str]:
    meta_path = Path(out_root) / "meta.json"
    if not meta_path.exists():
        raise FileNotFoundError(
            f"Missing {meta_path}. Run offline_ingest first to create OUT_ROOT/meta.json."
        )
    with meta_path.open("r", encoding="utf-8") as f:
        meta = json.load(f)
    weeks = meta.get("weeks_in_order")
    if not isinstance(weeks, list) or not all(isinstance(x, str) for x in weeks):
        raise ValueError("meta.json missing valid weeks_in_order list[str].")
    return weeks


def filter_weeks(weeks_in_order: List[str], requested: Optional[List[str]]) -> List[str]:
    if not requested:
        return weeks_in_order
    req = {extract_week_key_from_name(x) for x in requested}
    return [wk for wk in weeks_in_order if wk in req]


def daily_ob_paths_for_week(week_key: str, ob_by_day: dict[date, str]) -> List[str]:
    start_dt, end_dt, _ = _parse_week_key_any("BTCUSDT_OB_" + week_key)
    d0, d1 = start_dt.date(), end_dt.date()
    paths = []
    missing = []
    d = d0
    while d <= d1:
        p = ob_by_day.get(d)
        if p is None:
            missing.append(d.strftime("%Y-%m-%d"))
        else:
            paths.append(p)
        d += timedelta(days=1)
    if missing:
        raise ValueError(f"Missing daily OB files for week={week_key}: {missing}")
    return paths


def write_week_snapshots(out_root: str, week_key: str, series: SnapshotSeries, *, overwrite: bool) -> Path:
    week_dir = Path(out_root) / week_key
    week_dir.mkdir(parents=True, exist_ok=True)
    out_path = week_dir / "snapshots.npz"
    if out_path.exists() and not overwrite:
        raise FileExistsError(f"Snapshot file exists: {out_path} (use --overwrite)")
    series.to_npz(out_path)
    return out_path


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--ob-dir",
        default=OB_DIR,
        help="Directory containing daily OB zips like YYYY-MM-DD_BTCUSDT_ob200.data.zip",
    )
    parser.add_argument("--out-root", default=OUT_ROOT, help="Output root matching offline_ingest")
    parser.add_argument("--weeks", default=RAW_BYBIT_WEEKS, help="Comma-separated week keys")
    parser.add_argument("--overwrite", action="store_true", help="Overwrite existing snapshot files")
    args = parser.parse_args()

    weeks_in_order = load_weeks_in_order(args.out_root)
    requested = _parse_requested_weeks(args.weeks) if args.weeks else None
    weeks = filter_weeks(weeks_in_order, requested)
    if not weeks:
        raise ValueError(
            f"No matching weeks found for requested filter. "
            f"requested={requested or []} available={weeks_in_order}"
        )

    ob_by_day = _build_ob_daily_map(args.ob_dir)
    if not ob_by_day:
        raise FileNotFoundError(
            f"No daily OB files found in {args.ob_dir}. "
            "Expected files like YYYY-MM-DD_BTCUSDT_ob200.data.zip"
        )

    for wk in weeks:
        ob_paths = daily_ob_paths_for_week(wk, ob_by_day)
        print(
            f"[snapshots] week={wk} "
            f"ob={os.path.basename(ob_paths[0])}...{os.path.basename(ob_paths[-1])} "
            f"({len(ob_paths)} files)"
        )
        series = build_snapshots_from_ob_files(ob_paths)
        if not series.ts:
            print(f"  [skip] no snapshots produced for {wk}")
            continue
        out_path = write_week_snapshots(args.out_root, wk, series, overwrite=args.overwrite)
        print(f"  [write] {out_path} rows={len(series.ts):,}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
