def _ob_ext_rank(path: str) -> int:
    lower_path = str(path).lower()
    if lower_path.endswith(".data.zip"):
        return 0
    p = Path(path)
    if p.suffix.lower() == ".zip":
        return 1
    return 2


def _choose_preferred_daily_ob_path(candidates: List[str]) -> str:
    return min(candidates, key=lambda path: (_ob_ext_rank(path), Path(path).name, str(path)))


def _parse_requested_weeks(raw: str) -> List[str]:
    return [wk.strip() for wk in re.split(r"[\s,]+", raw) if wk.strip()]


def _parse_ymd_date(s: str) -> date:
    return datetime.strptime(s, "%Y-%m-%d").date()


def extract_week_key_from_name(name: str) -> str:
    m = re.search(r"\d{2}-\d{2}-\d{4}-to-\d{2}-\d{2}-\d{4}", name)
    if m:
        return m.group(0)
    raise ValueError(f"Could not extract week key from file name: {name}")


def _parse_week_key(week_key: str) -> Tuple[datetime, datetime, str]:
    m = re.fullmatch(r"(\d{2}-\d{2}-\d{4})-to-(\d{2}-\d{2}-\d{4})", week_key)
    if not m:
        raise ValueError(f"Unrecognized week key: {week_key}")
    s = datetime.strptime(m.group(1), "%d-%m-%Y")
    e = datetime.strptime(m.group(2), "%d-%m-%Y")
    return s, e, week_key


def _build_ob_daily_map(ob_dir: str) -> dict[date, str]:
    by_day: dict[date, str] = {}
    for path in sorted(Path(ob_dir).iterdir()):
        if not path.is_file():
            continue
        m = OB_DAILY_RE.match(path.name)
        if not m:
            continue
        day = _parse_ymd_date(m.group("d"))
        current = by_day.get(day)
        candidate = str(path)
        if current is None:
            by_day[day] = candidate
            continue
        chosen = _choose_preferred_daily_ob_path([current, candidate])
        if chosen != current:
            by_day[day] = candidate
        print(f"[warn] duplicate daily OB for {day.isoformat()}: {Path(current).name} vs {path.name}; using {Path(chosen).name}")
    return by_day


def _utc_day_bounds_ms(day: date) -> tuple[int, int]:
    start = datetime(day.year, day.month, day.day, tzinfo=timezone.utc)
    end = start + timedelta(days=1)
    return int(start.timestamp() * 1000), int(end.timestamp() * 1000)


